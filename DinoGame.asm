rect MACRO x, y, w, h, color
	mov ah, 06h ; background
	mov cl, x ; starting left upper x
	mov dh, y; starting left upper y
	;relative to cl
	mov dl, cl 
	add dl, w ;width
	mov ch, y ;?
	inc ch ;?
	mov al, h ; y unit up ; height?
	add dh, al; so that it looks like it's growing downward
	mov bh, color ; color
	int 10h ;for bios video service
