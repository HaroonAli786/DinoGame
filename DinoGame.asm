;compare current hi score with 3 previous hi score
cmphiscore PROC uses bx cx dx
  cmp pagearr[6], 1; the changes will happen only once when game is over
  je chsprocexit
	;sound
	mov al , 1
	cmp SongDoneorNot , al
	je chsExitsoundm
		call BEEP
		mov SongDoneorNot , 1
	chsExitsoundm:

	mov AX, score
	MOV BX, 10
	MOV si, 3
	MOV CX, 4  
chsL1:  ;convert current hi score to string
   MOV DX, 0000H    
   div BX
   ADD DX, 48
   mov strcurrhi[si], Dl 
   dec si          
   CMP AX, 0       
   JNE chsL1
	;find minimum prev hi score
	mov bx, 0
	mov si, 4
	mov cx, 4
	mov ah, 0
	chsl2:;compare first two numbers from 'str previoius hi'
		mov dl, strprevhi[bx]
		cmp dl, strprevhi[si]
		je chsexit
			jb chsbl
				mov ah, 4
				jmp chsl3
			chsbl:
				mov ah, 0
				jmp chsl3
		chsexit:
		inc bx
		inc si
	loop chsl2
	chsl3:

	mov bx, 0
	mov cx, 4
	mov bl, ah
	mov si, 8
	chsl4:;compare with 3rd prev hi number
		mov dl, strprevhi[bx]
		cmp dl, strprevhi[si]
		je chsexit1
			jb chsbl1
				mov ah, 8
				jmp chsl5
			chsbl1:
				jmp chsl5
		chsexit1:
		inc bx
		inc si
	loop chsl4
	chsl5:

	mov bx, 0
	mov bl, ah
	mov cx, 4
	mov si, 0
	chsl6:;compare prev lowest number from file with current score
		mov dl, strcurrhi[si]
		cmp dl, strprevhi[bx]
		je chsexit2
			jb chsbl2
				jmp chsl7
			chsbl2:
				mov ah, 1
				jmp chsl7
		chsexit2:
		inc bx
		inc si
	loop chsl6
	chsl7:

	mov bx, 0
	cmp ah, 1;ah=1 mean current score is lower than lowest prev hi score
	je chsexit3;no need to swap
		mov bl, ah
		mov cx, 4
		mov si, 0
		;mov current hi score to prev high score; to write in file
		chsl8:
			mov al, strcurrhi[si]
			mov strprevhi[bx], al
			inc si
			inc bx
		loop chsl8
		
		;write to the file
		writefile prevhifl, strprevhi
	chsexit3:
	mov pagearr[6], 1 ;so, when gameover then make changes to file(once)
chsprocexit:
ret
cmphiscore endp

